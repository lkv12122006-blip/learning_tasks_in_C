#include <iostream>
#include <cmath>

using namespace std;
int main()
{
	double r, f, x, y;
	system("chcp 1251");
	cout << "Введите расстояние от начала координат (полярный радиус):";
	cin >> r; // Ввод полярного радиуса. 
	cout << "Введите угол между полярной осью и лучом к точке (полярный угол, обычно в радианах):";
	cin >> f; // Ввод полярного угла. 
	x = r * cos(f); // Вычисление полярного радиуса.
	y = r * sin(f); // Вычисление полярного угла. 
	cout << "x: " << x << "\n";
	cout << "y: " << y;
	return 0;
}

